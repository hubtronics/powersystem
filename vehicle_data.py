def get_all_makes():
    """Return list of all vehicle makes"""
    return sorted(VEHICLE_MAKES_MODELS.keys())

def get_models_for_make(make):
    """Return list of models for a specific make"""
    return VEHICLE_MAKES_MODELS.get(make, [])

def get_all_models():
    """Return flat list of all models"""
    models = []
    for make_models in VEHICLE_MAKES_MODELS.values():
        models.extend(make_models)
    return sorted(models)
